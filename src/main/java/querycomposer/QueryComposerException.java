package querycomposer;

public class QueryComposerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public QueryComposerException() {
		super();
	}

	public QueryComposerException(String arg0) {
		super(arg0);
	}

	public QueryComposerException(Throwable arg0) {
		super(arg0);
	}

	public QueryComposerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public QueryComposerException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
